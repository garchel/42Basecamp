#include <unistd.h>

void	ft_print_comb2(void)
{	
	char	num[5];

	num[0] = '0' -1;
	num[2] = ' ';
	while (num[0]++ < '9')
	{
		num[1] = '0' -1;
		num[3] = num[0] -1;
		num[4] = num[1] +1;
		while (num[1]++ < '9')
		{
			while (num[3]++ < '9')
			{
				while (num[4]++ < '9')
				{
					write(1, &num, 5);
					if (num[0] != '9' || num[1] != '8')
						write(1, ", ", 2);
				}
				num[4] = '0' -1;
			}
			num[3] = num[0] - 1;
			num[4] = num[1] + 1;
		}
	}
}
